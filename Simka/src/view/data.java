    private void showTabelPegawai() throws SQLException{
            DefaultTableModel dtmPegawai = new DefaultTableModel(new String[]{"NIP", "ID INSTALASI", "NAMA PEGAWAI", "ALAMAT", "NO HP", "EMAIL"},0);
            dtmPegawai.setRowCount(0);
            
            for(Pegawai p : this.transaksi.getDataPegawai()){
                dtmPegawai.addRow(new String[]{p.getNip().toString(), 
                    p.getInstalasi().getId_Instalasi().toString(), 
                    p.getNama_Pegawai(), 
                    p.getAlamat(), 
                    p.getNo_hp(), 
                    p.getEmail()});
            }
            this.tData.setModel(dtmPegawai);
        }

