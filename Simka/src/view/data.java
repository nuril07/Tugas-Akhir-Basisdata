    private void btTambahMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btTambahMouseClicked
        // TODO add your handling code here:
        Pegawai p = new Pegawai();
        try {
                p.setNip(Integer.parseInt(this.tfNip.getText()));
                p.setInstalasi(this.transaksi.getDataInstalasi().get(Integer.parseInt(this.tfInstalasi.getText())));
                p.setNama_Pegawai(this.tfNpegawai.getText());
                p.setAlamat(this.tfAlamat.getText());
                p.setNo_hp(this.tfNohp.getText());
                p.setEmail(this.tfEmail.getText());
                this.transaksi.insertPegawai(p);
                this.showTabelPegawai();
            } catch (SQLException err) {
                System.out.println(err);
            }
    }//GEN-LAST:event_btTambahMouseClicked

